  pageSize: 10,
};
export default {
  name: "homeAdvertiseList",
  data() {
    return {
      tableHeight: 0, // 表格高度
      listQuery: Object.assign({}, defaultListQuery),
      list: null,
      total: null,
      listLoading: false,
      typeEnum: ['首页弹屏', "首页banner", "个人中心banner位", '个人中心广告位'],
