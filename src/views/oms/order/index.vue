<template>
   
  <div class="app-container">
    <el-card class="filter-container" shadow="never">
      <div style="margin-top: 15px">
        <el-form :inline="true" :model="listQuery" size="small" label-width="140px">
          <el-form-item label="用户ID：">
            <el-select v-model="listQuery.nickName" placeholder="请选择仓库类型" clearable class="input-width">
              <el-option v-for="item in statusOptions" :key="item.value" :label="item.label" :value="item.value">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item label="运单号：">
            <el-select v-model="listQuery.deliverySn" placeholder="请选择归属组织" clearable class="input-width">
              <el-option v-for="item in statusOptions" :key="item.value" :label="item.label" :value="item.value">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item label="转运单号：">
            <el-input v-model="listQuery.outDeliverySn" class="input-width" placeholder="请输入运单号"></el-input>
          </el-form-item>
          <el-form-item>
            <el-button style="float: right" type="primary" @click="handleSearchList()" size="small">
              查询搜索</el-button>
            <el-button style="float: right; margin-right: 15px" @click="handleResetSearch()" size="small">
              重置</el-button>
          </el-form-item>
        </el-form>
      </div>
    </el-card>
    <el-card class="operate-container" shadow="never">
      <i class="el-icon-tickets"></i>
      <span>数据列表</span>
      <span style="float: right">
        <el-button size="mini" type="primary" style="margin-left:20px;" @click="handleAdd()"> 新增 </el-button>
        <el-button size="mini" class="btn-add" @click="batchHandleProduct">批量删除</el-button>
      </span>
    </el-card>
    <div style="margin-top: 20px">
      <el-tabs v-model="activeName" type="card" @tab-click="selectStatus">
        <el-tab-pane v-for="(item, index) in statusOptions" :key="index" :label="item.label" :name="item.name">
        </el-tab-pane>
      </el-tabs>
    </div>
    <div class="table-container">
      <el-card class="operate-container" shadow="never">
        <div class="">
          <el-table ref="homeAdvertiseTable" :max-height="tableHeight" :data="list" style="width: 100%" @selection-change="handleSelectionChange" v-loading="listLoading" border>
            <el-table-column type="selection" width="60" align="center" fixed="left"></el-table-column>
            <el-table-column label="序号" width="60" align="center" fixed="left">
              <template slot-scope="scope">{{ scope.row.id }}</template>
            </el-table-column>
            <el-table-column label="订单号" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.orderSn }}</template>
            </el-table-column>
            <el-table-column label="用户ID" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.nickName }}</template>
            </el-table-column>
            <el-table-column label="转运单号" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.outDeliverySn }}</template>
            </el-table-column>
            <el-table-column label="重量(kg)" align="center" width="150">
              <template slot-scope="scope">{{scope.row.weight }}</template>
            </el-table-column>
            <el-table-column label="长宽高(cm)" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.length*scope.row.width*scope.row.height }}</template>
            </el-table-column>
            <el-table-column label="服务费(฿)" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.createTime || "-" }}</template>
            </el-table-column>
            <el-table-column label="最终运费(฿)" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.totalAmount || "-" }}</template>
            </el-table-column>

            <el-table-column label="离开仓库日期" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.deliveryTime || "-" }}</template>
            </el-table-column>
            <el-table-column label="泰国收件人信息" align="center" width="150">
              <template slot-scope="scope">
                <div>姓名：{{scope.row.receiverName}}</div>
                <div>手机：{{scope.row.receiverPhone}}</div>
                <div>地址：{{scope.row.receiverDetailAddress}}</div>
              </template>
            </el-table-column>
            <el-table-column label="物流状态" align="center" width="150">
              <template slot-scope="scope">{{ scope.row.createTime  }}</template>
            </el-table-column>

            <el-table-column label="操作" width="250" align="center" fixed="right">
              <template slot-scope="scope">
                <div>
                  <el-button size="mini" type="warning" @click="handleUpdate(scope.$index, scope.row)">催付
                  </el-button>
                  <el-button size="mini" type="primary" @click="handleUpdate(scope.$index, scope.row)">编辑
                  </el-button>
                  <el-button size="mini" type="danger" @click="handleDelete(scope.$index, scope.row)">删除
                  </el-button>
                </div>
                <div>
                  <el-button size="mini" type="success" plain @click="handleUpdate(scope.$index, scope.row)">到达泰国
                  </el-button>
                  <el-button size="mini" type="success" @click="handleUpdate(scope.$index, scope.row)">完成派送
                  </el-button>
                </div>
              </template>
            </el-table-column>
          </el-table>
        </div>
      </el-card>
    </div>
    <div class="pagination-container">
      <el-pagination background @size-change="handleSizeChange" @current-change="handleCurrentChange" layout="total, sizes,prev, pager, next,jumper" :page-size="listQuery.pageSize" :page-sizes="[5, 10, 15]" :current-page.sync="listQuery.pageNum" :total="total">
      </el-pagination>
    </div>
  </div>
</template>
<script>
import { fetchList } from "@/api/order";
import { formatDate } from "@/utils/date";
const defaultListQuery = {
  pageNum: 1,
  pageSize: 10,
};
export default {
  name: "homeAdvertiseList",
  data() {
    return {
      listQuery: Object.assign({}, defaultListQuery),
      tableHeight: 0, // 表格高度
      list: [],
      total: 0,
      listLoading: false,
      multipleSelection: [], // 当前选择的列表
      activeName: "all",
      statusOptions: [
        {
          label: "全部",
          value: null,
          name: "all",
        },
        {
          label: "已入库",
          value: 0,
          name: "wait_pay",
        },
        {
          label: "待支付",
          value: 1,
          name: "wait_sent",
        },
        {
          label: "派送中",
          value: 3,
          name: "wait_get",
        },
        {
          label: "已签收",
          value: 4,
          name: "already_cancel",
        },
      ],
    };
  },
  created() {
    this.getList();
  },
  mounted() {
    this.$nextTick(() => {
      const h = document.body.clientHeight;
      this.tableHeight = h - 100;
    });
  },
  methods: {

    // 重置
    handleResetSearch() {
      this.listQuery = Object.assign({}, defaultListQuery);
    },
    // 搜索
    handleSearchList() {
      this.listQuery.pageNum = 1;
      this.getList();
    },
    // 根据状态查询
    selectStatus() {
      this.listQuery["status"] = this.statusOptions.find(
        (i) => i.name == this.activeName
      ).value;
      this.getList();
    },
    // 分页 修改size
    handleSizeChange(val) {
      this.listQuery.pageNum = 1;
      this.listQuery.pageSize = val;
      this.getList();
    },
    // 切换分页
    handleCurrentChange(val) {
      this.listQuery.pageNum = val;
      this.getList();
    },
    // 删除订单
    handleDelete(index, row) {
      this.deleteHomeAdvertise(row.id);
    },
    // 添加订单
    handleAdd() {
      this.$router.push({ path: "/oms/orderDetail" });
    },
    // 编辑订单
    handleUpdate(index, row) {
      this.$router.push({
        path: "/oms/orderDetail",
        query: { id: row.id },
      });
    },
    // 获取列表
    getList() {
      this.listLoading = true;
      let listQuery = JSON.parse(JSON.stringify(this.listQuery));
      fetchList(listQuery).then((response) => {
        this.listLoading = false;
        if (response.data.list) {
          this.list = response.data.list || [];
        }
        this.total = response.data.total;
      });
    },
    deleteHomeAdvertise(ids) {
      this.$confirm("是否要删除该条订单?", "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      }).then(() => {

        let params = new URLSearchParams();
        params.append("ids", ids);
        console.log('ids', params)
        deleteHomeAdvertise(params).then((response) => {
          this.$message({
            type: "success",
            message: "删除成功!",
          });
          if (this.list && this.list.length === 1 && this.listQuery.pageNum > 1) {
            this.listQuery.pageNum--;
          }
          this.getList();
        });
      });
    },
    // 选择列表
    handleSelectionChange(val) {
      this.multipleSelection = val;
    },
    // 批量删除
    batchHandleProduct() {
      let ids = [];
      console.log("multipleSelection", this.multipleSelection);
      // 处理选中的商品id
      this.multipleSelection &&
        this.multipleSelection.map((item) => {
          ids.push(item.id);
        });
      if (this.multipleSelection.length < 1) {
        this.$message({
          message: "请选选中商品后再进行批量操作！",
          type: "error",
          duration: 1000,
        });
        return;
      }
      this.$confirm("是否要进行批量操作?", "提示", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning",
      }).then(() => {

      });
    },
  },
};
</script>
